# ---------------------------------------------------------------------------
# TrueType font generation
# ---------------------------------------------------------------------------

# Metrics match DejaVu Sans Mono Bold so the existing ratio calibration (2.317)
# for Font D in font.rs continues to produce correct advance widths.
UPM = 2048
ASCENT = 1901
DESCENT = -483
H_ADVANCE = 1233  # matches DejaVu Mono Bold advance

COLS = 12   # pixel columns per glyph cell
ROWS = 14   # visible pixel rows (rows 14-17 are blank descender space)

# Native bitmap cell size (Font D: 18 dots high, 10 dots nominal advance).
NATIVE_FONT_SIZE = 18   # dots
BITMAP_SCALE_CORRECTION = 7.0 / 6.0   # scale.y correction applied in renderer.rs

# Y_SCALE: font units per pixel row such that each row renders as 1 pixel at native
# size after the 7/6 scale correction is applied.
#   renderer: scale.y = NATIVE_FONT_SIZE × 7/6 = 21
#   1 pixel at scale.y=21 ⟹ UPM / scale.y = 2048/21 ≈ 97.5 font units
Y_SCALE = UPM / (NATIVE_FONT_SIZE * BITMAP_SCALE_CORRECTION)   # ≈ 97.52

# X_SCALE: font units per pixel column (advance / 12 columns)
X_SCALE = H_ADVANCE / COLS        # ≈ 102.75 units per pixel column


def bitmap_to_contours(rows):
    """Convert a list of pixel rows into a list of TrueType contour point lists.

    Each 'on' pixel becomes a filled rectangle in font coordinates.
    TrueType y-axis points upward; row 0 is the topmost pixel.
    """
    from fontTools.pens.ttGlyphPen import TTGlyphPen

    pen = TTGlyphPen(None)

    for row_idx, row in enumerate(rows):
        for col_idx, pixel in enumerate(row):
            if not pixel:
                continue
            # Font coordinates (y upward)
            x0 = round(col_idx * X_SCALE)
            x1 = round((col_idx + 1) * X_SCALE)
            y1 = round(ASCENT - row_idx * Y_SCALE)
            y0 = round(ASCENT - (row_idx + 1) * Y_SCALE)

            # Draw a counter-clockwise rectangle (TrueType fill rule)
            pen.beginPath()
            pen.endPath()
            pen.beginPath()
            pen.qCurveTo()

            # Use moveTo / lineTo interface
            pen.beginPath()
            pen.endPath()

    # Use the simpler interface below instead
    return None


def make_glyph(char, bitmap_rows, glyph_set):
    """Build a TTGlyph from a bitmap."""
    from fontTools.pens.ttGlyphPen import TTGlyphPen

    pen = TTGlyphPen(glyph_set)

    for row_idx, row in enumerate(bitmap_rows):
        for col_idx, pixel in enumerate(row):
            if not pixel:
                continue
            x0 = round(col_idx * X_SCALE)
            x1 = round((col_idx + 1) * X_SCALE)
            y1 = round(ASCENT - row_idx * Y_SCALE)
            y0 = round(ASCENT - (row_idx + 1) * Y_SCALE)

            if y0 >= y1:
                continue  # degenerate rectangle

            # Counter-clockwise winding (TrueType positive fill)
            pen.beginPath()
            pen.endPath()

    return pen.glyph()


def build_font(output_path):
    from fontTools.fontBuilder import FontBuilder
    from fontTools.ttLib import TTFont

    # Collect all characters we have bitmaps for
    chars = sorted(BITMAPS.keys())
    # Prepend .notdef
    glyph_order = [".notdef"] + [f"uni{ord(c):04X}" for c in chars]
    cmap = {ord(c): f"uni{ord(c):04X}" for c in chars}

    fb = FontBuilder(UPM, isTTF=True)
    fb.setupGlyphOrder(glyph_order)
    fb.setupCharacterMap(cmap)

    # Build glyphs
    from fontTools.pens.ttGlyphPen import TTGlyphPen

    glyphs = {}
    advance_widths = {}

    # .notdef: empty box
    pen = TTGlyphPen(None)
    m = round(0.1 * H_ADVANCE)
    pen.moveTo((m, 0))
    pen.lineTo((H_ADVANCE - m, 0))
    pen.lineTo((H_ADVANCE - m, ASCENT))
    pen.lineTo((m, ASCENT))
    pen.closePath()
    pen.moveTo((m + m, m))
    pen.lineTo((m + m, ASCENT - m))
    pen.lineTo((H_ADVANCE - m - m, ASCENT - m))
    pen.lineTo((H_ADVANCE - m - m, m))
    pen.closePath()
    glyphs[".notdef"] = pen.glyph()
    advance_widths[".notdef"] = H_ADVANCE

    for ch in chars:
        gname = f"uni{ord(ch):04X}"
        bitmap = BITMAPS[ch]
        pen = TTGlyphPen(None)

        has_ink = False
        for row_idx, row in enumerate(bitmap):
            for col_idx, pixel in enumerate(row):
                if not pixel:
                    continue
                has_ink = True
                x0 = round(col_idx * X_SCALE)
                x1 = round((col_idx + 1) * X_SCALE)
                y1 = round(ASCENT - row_idx * Y_SCALE)
                y0 = round(ASCENT - (row_idx + 1) * Y_SCALE)
                if y0 >= y1 or x0 >= x1:
                    continue
                # Counter-clockwise = filled in TrueType
                pen.moveTo((x0, y0))
                pen.lineTo((x0, y1))
                pen.lineTo((x1, y1))
                pen.lineTo((x1, y0))
                pen.closePath()

        if has_ink:
            glyphs[gname] = pen.glyph()
        else:
            # Empty glyph (space, etc.)
            glyphs[gname] = pen.glyph()

        advance_widths[gname] = H_ADVANCE

    fb.setupGlyf(glyphs)
    # setupHorizontalMetrics expects {glyph_name: (advance_width, lsb)}
    metrics = {gname: (w, 0) for gname, w in advance_widths.items()}
    fb.setupHorizontalMetrics(metrics)

    fb.setupHorizontalHeader(ascent=ASCENT, descent=DESCENT)
    fb.setupNameTable({
        "familyName": "ZebraFontD",
        "styleName": "Regular",
    })
    fb.setupOS2(
        sTypoAscender=ASCENT,
        sTypoDescender=DESCENT,
        sTypoLineGap=0,
        usWinAscent=ASCENT,
        usWinDescent=abs(DESCENT),
        fsType=0,
        achVendID="LBLE",
    )
    fb.setupPost()
    fb.setupHead(unitsPerEm=UPM)

    fb.font.save(output_path)
    print(f"Saved {output_path}")
    print(f"  Glyphs: {len(glyph_order)}")
    print(f"  UPM={UPM}, ascent={ASCENT}, descent={DESCENT}, h_advance={H_ADVANCE}")


if __name__ == "__main__":
    out = sys.argv[1] if len(sys.argv) > 1 else "ZebraFontD.ttf"
    build_font(out)
